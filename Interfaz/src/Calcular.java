public class Calcular implements Calculadora {
	public Calcular() {
	}
	
	
	 public int sumar(int n1,int n2) {
		return n1+n2;
	}
	
	 public int resta(int n1,int n2) {
		return n1-n2;
	}
	
	 public int mul(int n1,int n2) {
		return n1*n2;
	}
	
	public double div(double n1,double n2) {
		return n1/n2;
	}
	
}
